/**
 * Secondary sidebar mounted next to the main `<Sidebar>`. Has two
 * modes: "threads" (default — recent threads grouped by session) and
 * "messages" (recent message snippets with thread context). Both
 * group by session so the temporal scaffold matches across modes.
 *
 * Open/closed and mode preferences persist in localStorage so a user
 * who lives in "messages" mode doesn't get bumped back to "threads"
 * on every nav.
 */
export function RecentChatsRail({ workspaceId }: { workspaceId: string }) {
  const router = useRouter()
  const pathname = usePathname()
  const { enabled: wayseerEnabled } = useWayseer()
  const [open, setOpen] = React.useState<boolean | null>(null)
  const [mode, setMode] = React.useState<RailMode>("threads")
  const [threadGroups, setThreadGroups] = React.useState<
    ThreadGroup[] | null
  >(null)
  const [exchangeGroups, setExchangeGroups] = React.useState<
    ExchangeGroup[] | null
  >(null)
  const [error, setError] = React.useState<string | null>(null)
  const [wayseerSnippets, setWayseerSnippets] = React.useState<
    Record<string, string>
  >({})
  // Set of thread ids whose operator typed the configured done
  // sentinel as a user turn. Source of truth lives in the API
  // response; the rail dims matching rows the same way the Work tab
  // does, so the "completed" signal is consistent across surfaces.
  const [doneThreadIds, setDoneThreadIds] = React.useState<Set<string>>(
    () => new Set()
  )

  // Hydrate persisted prefs once on mount.
  React.useEffect(() => {
    const storedOpen = window.localStorage.getItem(STORAGE_KEY_OPEN)
    setOpen(storedOpen === null ? true : storedOpen === "1")
    const storedMode = window.localStorage.getItem(STORAGE_KEY_MODE)
    if (storedMode === "messages" || storedMode === "threads") {
      setMode(storedMode)
    }
  }, [])

  React.useEffect(() => {
    if (open === null) return
    window.localStorage.setItem(STORAGE_KEY_OPEN, open ? "1" : "0")
  }, [open])

  React.useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY_MODE, mode)
  }, [mode])

  // Refetch when workspace or mode changes. Each mode owns its own
  // cache so flipping back to a previously-loaded mode is instant.
  React.useEffect(() => {
    let cancelled = false
    setError(null)
    // In showcase mode the rail reads from a static JSON snapshot
    // (no API runtime). The JSON files are generated by
    // `scripts/showcase-snapshot.ts` and shipped to `public/showcase-data/`.
    const url = SHOWCASE_MODE
      ? mode === "messages"
        ? "/showcase-data/recent-with-exchanges.json"
        : "/showcase-data/recent-with-threads.json"
      : mode === "messages"
        ? "/api/operator-studio/sessions/recent-with-threads?mode=messages&limit=8"
        : "/api/operator-studio/sessions/recent-with-threads?limit=8"
    if (mode === "messages") setExchangeGroups(null)
    else setThreadGroups(null)

    fetch(url)
      .then((r) => (r.ok ? r.json() : Promise.reject(r.statusText)))
      .then((data) => {
        if (cancelled) return
        if (mode === "messages") setExchangeGroups(data.groups ?? [])
        else setThreadGroups(data.groups ?? [])
        setDoneThreadIds(
          new Set<string>(
            Array.isArray(data.doneThreadIds) ? data.doneThreadIds : []
          )
        )
      })
      .catch((err) => {
        if (cancelled) return
        setError(typeof err === "string" ? err : "Failed to load")
      })
    return () => {
      cancelled = true
    }
  }, [workspaceId, mode])

  // Batch-fetch Wayseer enrichment snippets for the threads currently
  // shown in the rail. Only when Wayseer is enabled — when off, the
  // snippet line is hidden entirely. Refetches whenever the visible
  // thread set changes.
  React.useEffect(() => {
    // No wayseer enrichments in the static showcase — endpoint
    // doesn't exist and the snippets are LLM-generated, not part of
    // the snapshot.
    if (SHOWCASE_MODE || !wayseerEnabled) {
      setWayseerSnippets({})
      return
    }
    if (mode !== "threads" || !threadGroups) return
    const ids = threadGroups
      .flatMap((g) => g.threads.map((t) => t.id))
      .slice(0, 50)
    if (ids.length === 0) {
      setWayseerSnippets({})
      return
    }
    let cancelled = false
    fetch(
      `/api/operator-studio/wayseer/enrichments?threadIds=${ids
        .map(encodeURIComponent)
        .join(",")}`
    )
      .then((r) => (r.ok ? r.json() : Promise.reject(r.statusText)))
      .then((data) => {
        if (cancelled) return
        setWayseerSnippets(
          (data?.snippets as Record<string, string> | undefined) ?? {}
        )
      })
      .catch(() => {
        if (!cancelled) setWayseerSnippets({})
      })
    return () => {
      cancelled = true
    }
  }, [wayseerEnabled, mode, threadGroups])

  // Pre-warm the hover-preview cache for every thread visible in the
  // rail so first hover is instant. Bulk endpoint returns just bookend
  // messages (first/last user + assistant) — cheap even for huge threads.
  React.useEffect(() => {
    if (mode !== "threads" || !threadGroups) return
    const ids = threadGroups
      .flatMap((g) => g.threads.map((t) => t.id))
      .filter((id) => !previewCache.has(id))
    if (ids.length === 0) return
    primeThreadPreviews(ids)
  }, [mode, threadGroups])

  if (open === null) return null

  if (!open) {
    return (
      <aside
        aria-label="Recent chats (collapsed)"
        className="hidden md:flex h-svh w-8 shrink-0 flex-col items-center border-r bg-sidebar/60 py-2"
      >
        <button
          type="button"
          onClick={() => setOpen(true)}
          aria-label="Show recent chats"
          title="Show recent chats"
          className="flex h-8 w-8 items-center justify-center rounded-md text-muted-foreground hover:bg-sidebar-accent hover:text-sidebar-accent-foreground"
        >
          <History className="size-4" />
        </button>
        <button
          type="button"
          onClick={() => setOpen(true)}
          aria-label="Expand recent chats"
          className="mt-1 flex h-8 w-8 items-center justify-center rounded-md text-muted-foreground hover:bg-sidebar-accent hover:text-sidebar-accent-foreground"
        >
          <ChevronRight className="size-3.5" />
        </button>
      </aside>
    )
  }

  return (
    <aside
      aria-label="Recent chats"
      className="hidden md:flex h-svh w-64 shrink-0 flex-col border-r bg-sidebar/60"
    >
      <div className="flex h-10 shrink-0 items-center gap-2 border-b px-3">
        <History className="size-3.5 text-muted-foreground" />
        <span className="text-[11px] font-medium uppercase tracking-wider text-muted-foreground">
          Recent
        </span>
        <button
          type="button"
          onClick={() => setOpen(false)}
          aria-label="Hide recent chats"
          title="Hide recent chats"
          className="ml-auto flex h-6 w-6 items-center justify-center rounded text-muted-foreground hover:bg-sidebar-accent hover:text-sidebar-accent-foreground"
        >
          <ChevronLeft className="size-3.5" />
        </button>
      </div>
      <ModeTabs mode={mode} onChange={setMode} />
      <div className="min-h-0 flex-1 overflow-y-auto px-1.5 py-2">
        {error && (
          <p className="px-2 py-3 text-xs text-muted-foreground italic">
            Couldn’t load recent activity.
          </p>
        )}
        {!error && mode === "threads" && (
          <ThreadsView
            groups={threadGroups}
            activePath={pathname}
            onNavigate={(href) => router.push(href)}
            wayseerSnippets={wayseerSnippets}
            doneThreadIds={doneThreadIds}
          />
        )}
        {!error && mode === "messages" && (
          <ExchangesView
            groups={exchangeGroups}
            activePath={pathname}
            onNavigate={(href) => router.push(href)}
            doneThreadIds={doneThreadIds}
          />
        )}
      </div>
    </aside>
  )
}

