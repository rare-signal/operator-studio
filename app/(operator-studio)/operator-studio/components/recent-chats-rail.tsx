// ─── Session header ──────────────────────────────────────────────────────────

type TimeBucket =
  | "overnight"
  | "morning"
  | "afternoon"
  | "evening"
  | "late-night"
  | "all-day"

/**
 * Re-derive the session's time-of-day bucket from its actual span so
 * the icon/colour are reliable even when a session has a custom
 * `label`. Mirrors the bucketing rules in `defaultSessionLabel` —
 * keep them in sync.
 */
function sessionBucket(startedAt: string, endedAt: string): TimeBucket {
  const start = new Date(startedAt)
  const end = new Date(endedAt)
  const durationHours =
    (end.getTime() - start.getTime()) / 3_600_000
  if (durationHours >= 10) return "all-day"
  const h = start.getHours()
  if (h < 5) return "overnight"
  if (h < 12) return "morning"
  if (h < 17) return "afternoon"
  if (h < 21) return "evening"
  return "late-night"
}

