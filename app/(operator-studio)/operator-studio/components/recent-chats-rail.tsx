// ─── Threads view ────────────────────────────────────────────────────────────

function ThreadsView({
  groups,
  activePath,
  onNavigate,
  wayseerSnippets,
  doneThreadIds,
}: {
  groups: ThreadGroup[] | null
  activePath: string
  onNavigate: (href: string) => void
  wayseerSnippets: Record<string, string>
  doneThreadIds: Set<string>
}) {
  if (groups === null) {
    return (
      <p className="px-2 py-3 text-xs text-muted-foreground italic">Loading…</p>
    )
  }
  if (groups.length === 0) {
    return (
      <p className="px-2 py-3 text-xs text-muted-foreground italic">
        No sessions yet.
      </p>
    )
  }
  return (
    <>
      {groups.map(({ session, threads }) => (
        <div key={session.id} className="mb-3">
          <SessionHeader
            session={session}
            count={threads.length}
            activePath={activePath}
            onNavigate={onNavigate}
          />
          {threads.length === 0 ? (
            <p className="px-2 py-1 text-[11px] italic text-muted-foreground/70">
              No threads.
            </p>
          ) : (
            <ul className="mt-0.5">
              {[...threads]
                .sort(
                  (a, b) =>
                    new Date(b.updatedAt).getTime() -
                    new Date(a.updatedAt).getTime()
                )
                .map((t) => {
                  const title =
                    t.promotedTitle ?? t.rawTitle ?? "Untitled thread"
                  const href = `/operator-studio/threads/${t.id}`
                  const isActive = activePath === href
                  const snippet = wayseerSnippets[t.id]
                  const isDone = doneThreadIds.has(t.id)
                  return (
                    <li key={t.id}>
                      <ThreadHoverPreview
                        threadId={t.id}
                        title={title}
                        sourceApp={t.sourceApp}
                      >
                        <button
                          type="button"
                          onClick={() => onNavigate(href)}
                          className={cn(
                            "flex w-full flex-col gap-0.5 rounded px-2 py-1 text-left text-[12px]",
                            isActive
                              ? "bg-sidebar-accent text-sidebar-accent-foreground"
                              : isDone
                                ? "bg-emerald-50/70 dark:bg-emerald-950/40 opacity-70 hover:opacity-90 hover:bg-emerald-100/70 dark:hover:bg-emerald-950/60"
                                : "text-foreground/80 hover:bg-sidebar-accent/50 hover:text-foreground"
                          )}
                        >
                          <span className="flex w-full items-center gap-1.5">
                            <SourceAppToken
                              source={t.sourceApp}
                              variant="plain"
                              size="sm"
                              showLabel={false}
                              className="shrink-0"
                            />
                            <span className="min-w-0 flex-1 truncate">
                              {title}
                            </span>
                            {isDone && (
                              <CheckCircle2
                                className="h-3 w-3 shrink-0 text-emerald-500 dark:text-emerald-400"
                                aria-label="Marked done"
                              />
                            )}
                            <span className="shrink-0 tabular-nums text-[10px] text-muted-foreground/70">
                              {relativeTime(t.updatedAt)}
                            </span>
                          </span>
                          {snippet && (
                            <span className="ml-[1.4rem] truncate text-[10.5px] leading-snug text-muted-foreground/80">
                              {snippet}
                            </span>
                          )}
                        </button>
                      </ThreadHoverPreview>
                    </li>
                  )
                })}
            </ul>
          )}
        </div>
      ))}
    </>
  )
}

// ─── Exchanges view ──────────────────────────────────────────────────────────

function ExchangesView({
  groups,
  activePath,
  onNavigate,
  doneThreadIds,
}: {
  groups: ExchangeGroup[] | null
  activePath: string
  onNavigate: (href: string) => void
  doneThreadIds: Set<string>
}) {
  if (groups === null) {
    return (
      <p className="px-2 py-3 text-xs text-muted-foreground italic">Loading…</p>
    )
  }
  const nonEmpty = groups.filter((g) => g.exchanges.length > 0)
  if (nonEmpty.length === 0) {
    return (
      <p className="px-2 py-3 text-xs text-muted-foreground italic">
        No exchanges yet.
      </p>
    )
  }
  return (
    <>
      {nonEmpty.map(({ session, exchanges }) => (
        <div key={session.id} className="mb-3">
          <SessionHeader
            session={session}
            count={exchanges.length}
            activePath={activePath}
            onNavigate={onNavigate}
          />
          <ul className="mt-1 space-y-1.5">
            {exchanges.map((ex) => (
              <ExchangeCard
                key={ex.id}
                exchange={ex}
                activePath={activePath}
                onNavigate={onNavigate}
                isDone={doneThreadIds.has(ex.threadId)}
              />
            ))}
          </ul>
        </div>
      ))}
    </>
  )
}

/**
 * One exchange = a user prompt + the assistant response, rendered as
 * a single capsule. Each side gets a tight 2-line preview (~120 chars
 * worth) so the back-and-forth is legible at a glance without
 * blowing out the 16rem rail.
 *
 * Click the whole card → jump to the thread. The footer line carries
 * thread title / time / source so the card stays self-contained.
 */
function ExchangeCard({
  exchange,
  activePath,
  onNavigate,
  isDone,
}: {
  exchange: RecentExchange
  activePath: string
  onNavigate: (href: string) => void
  isDone: boolean
}) {
  const href = `/operator-studio/threads/${exchange.threadId}`
  const isActive = activePath === href
  const userSnippet = exchange.user ? trimSnippet(exchange.user.content) : null
  const assistantSnippet = exchange.assistant
    ? trimSnippet(exchange.assistant.content)
    : null
  const tooltip = [
    userSnippet ? `You: ${userSnippet}` : null,
    assistantSnippet ? `AI: ${assistantSnippet}` : null,
    `— ${exchange.threadTitle} · ${formatAbsolute(exchange.lastActivityAt)}`,
  ]
    .filter(Boolean)
    .join("\n\n")

  return (
    <li>
      <button
        type="button"
        onClick={() => onNavigate(href)}
        title={tooltip}
        className={cn(
          "flex w-full flex-col gap-1 rounded-md border px-2 py-1.5 text-left transition-colors",
          isActive
            ? "border-sidebar-accent bg-sidebar-accent text-sidebar-accent-foreground"
            : isDone
              ? "border-emerald-200/60 bg-emerald-50/70 dark:border-emerald-900/40 dark:bg-emerald-950/40 opacity-70 hover:opacity-90"
              : "border-border/40 bg-background/40 hover:border-border hover:bg-sidebar-accent/40"
        )}
      >
        {userSnippet && (
          <ExchangeLine
            role="user"
            content={userSnippet}
            isActive={isActive}
          />
        )}
        {userSnippet && assistantSnippet && (
          <div className="h-px bg-border/40" aria-hidden />
        )}
        {assistantSnippet && (
          <ExchangeLine
            role="assistant"
            content={assistantSnippet}
            isActive={isActive}
          />
        )}
        <div className="flex w-full items-center gap-1.5 pt-0.5">
          <span className="min-w-0 flex-1 truncate text-[10px] text-muted-foreground/80">
            {exchange.threadTitle}
          </span>
          <span className="shrink-0 tabular-nums text-[10px] text-muted-foreground/60">
            {relativeTime(exchange.lastActivityAt)}
          </span>
          <SourceAppToken
            source={exchange.threadSourceApp}
            variant="plain"
            size="sm"
            showLabel={false}
            className="shrink-0"
          />
        </div>
      </button>
    </li>
  )
}

