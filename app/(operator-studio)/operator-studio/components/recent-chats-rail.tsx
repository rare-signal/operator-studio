/**
 * Receipt-rip divider: a perforated line with the elapsed time and
 * a count of hidden messages in the middle. Visually evokes
 * "...time passes..." between the opening and closing exchanges.
 */
function ReceiptRip({
  fromIso,
  toIso,
  middleCount,
}: {
  fromIso: string
  toIso: string
  middleCount: number
}) {
  const gap = formatGap(fromIso, toIso)
  const hidden =
    middleCount > 0
      ? `${middleCount} ${middleCount === 1 ? "message" : "messages"} hidden`
      : null
  return (
    <div
      aria-hidden
      className="flex items-center gap-2 px-0.5 text-muted-foreground/70"
    >
      <Perforation />
      <div className="flex shrink-0 items-center gap-1 text-[9.5px] uppercase tracking-wider">
        <Hourglass className="size-2.5" />
        <span className="tabular-nums">{gap}</span>
        {hidden && (
          <>
            <span aria-hidden>·</span>
            <span className="tabular-nums">{hidden}</span>
          </>
        )}
      </div>
      <Perforation />
    </div>
  )
}

function Perforation() {
  return (
    <div
      className="h-px flex-1"
      style={{
        backgroundImage:
          "repeating-linear-gradient(to right, currentColor 0 3px, transparent 3px 7px)",
      }}
    />
  )
}

function formatGap(fromIso: string, toIso: string): string {
  const ms = new Date(toIso).getTime() - new Date(fromIso).getTime()
  if (!Number.isFinite(ms) || ms <= 0) return "—"
  const sec = Math.round(ms / 1000)
  if (sec < 60) return `${sec}s`
  const mins = Math.round(sec / 60)
  if (mins < 60) return `${mins}m`
  const hours = Math.floor(mins / 60)
  const remMin = mins % 60
  if (hours < 24) return remMin === 0 ? `${hours}h` : `${hours}h ${remMin}m`
  const days = Math.floor(hours / 24)
  const remHrs = hours % 24
  return remHrs === 0 ? `${days}d` : `${days}d ${remHrs}h`
}
